CREATE TABLE IF NOT EXISTS Clips (
		idx INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
		Name NVARCHAR(1024),
		Body TEXT,
		Category NVARCHAR(256), 
		Foreground BIGINT, 
		Background BIGINT);


CREATE TABLE IF NOT EXISTS Preferences (
		Name NVARCHAR(256) NOT NULL PRIMARY KEY,
		SValue NVARCHAR(1025),
		IValue BIGINT);

